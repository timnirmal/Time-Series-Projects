# Time-Series

# Time-Series means that the data is organized in time.

# Cross-Sectional Analysis - If you analyze the sale by-product on a particular day, for example, if you want to find
# out what’s the number 1 selling item on Christmas Eve.
#
# Time-Series Analysis - If you want to find out the sale of
# one particular item such as PS4 over a period of time (let’s say last 5 years)

############### Time-Series Forecasting Methods
# Time series forecasting can broadly be categorized into the following categories:
# Classical / Statistical Models — Moving Averages, Exponential smoothing, ARIMA, SARIMA, TBATS
# Machine Learning — Linear Regression, XGBoost, Random Forest, or any ML model with reduction methods
# Deep Learning — RNN, LSTM

# Depended Variable - Outcome Variable, Target
# Independent Variable - Predictor Variable, Input, Features

